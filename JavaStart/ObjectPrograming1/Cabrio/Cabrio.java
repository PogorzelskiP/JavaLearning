package JavaStart.ObjectPrograming1.Cabrio;

public class Cabrio {
    String name;
    String manufacturer;
    int topSpeed;
    boolean roof;

    public Cabrio(String name, String manufacturer) {
        this.name = name;
        this.manufacturer = manufacturer;
    }

    public Cabrio(String name, String manufacturer, int topSpeed, boolean roof) {
        this.name = name;
        this.manufacturer = manufacturer;
        this.topSpeed = topSpeed;
        this.roof = roof;
    }

    
}
